/* Write a JAVA program to print all alphabets from a to z. - using while loop  */

class Alphabets
  {
    public static void main(String args[])
    {
      
      System.out.println("Alphabets are: ");
      char ch = 'a';
      while(ch<='z')
        {
          System.out.println(ch);
          ch++;
        }
    }
  }